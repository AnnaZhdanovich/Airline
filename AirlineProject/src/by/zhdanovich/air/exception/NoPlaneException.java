package by.zhdanovich.air.exception;

public class NoPlaneException extends Exception{


	public NoPlaneException() {
		super();
		
	}

	public NoPlaneException(String message, Throwable e) {
		super(message, e);
		
	}

	public NoPlaneException(String message) {
		super(message);
		
	}

	
	

}
